// Generates lib/education-data.ts — real Census 2021 qualifications for all 69
// Birmingham wards, pulled from NOMIS dataset NM_2084_1 (TS067).
//
// Run: node scripts/fetch-education-data.mjs
//
// Census 2021 is a frozen dataset (next census 2031), so this is committed and
// read directly — no live fetch, no fallback. Re-run only if ONS restates TS067.
import { writeFileSync } from 'node:fs';

const CODES = Array.from({ length: 69 }, (_, i) => 'E0' + (5011118 + i));

// C2021_HIQUAL_8 category -> EducationWard field
const CAT = {
  1: 'qual_none', 2: 'qual_level1', 3: 'qual_level2', 4: 'qual_apprenticeship',
  5: 'qual_level3', 6: 'qual_level4plus', 7: 'qual_other',
};

const url = 'https://www.nomisweb.co.uk/api/v01/dataset/NM_2084_1.data.json'
  + `?geography=${CODES.join(',')}`
  + '&c2021_hiqual_8=0,1,2,3,4,5,6,7&measures=20100&date=latest'
  + '&select=geography_code,geography_name,c2021_hiqual_8,obs_value';

const res = await fetch(url, { signal: AbortSignal.timeout(60000) }).then(r => r.json());
if (res.error) throw new Error('NOMIS: ' + res.error);

const wards = {};
for (const o of res.obs) {
  const code = o.geography.geogcode;
  const cat = o.c2021_hiqual_8.value;
  const count = o.obs_value.value;
  const w = (wards[code] ??= {
    ward_code: code,
    ward_name: o.geography.description.replace(/ \(Birmingham\)$/, ''),
    total: 0, qual_none: 0, qual_level1: 0, qual_level2: 0,
    qual_apprenticeship: 0, qual_level3: 0, qual_level4plus: 0, qual_other: 0,
    skills_decile: 0, edu_rank: 0,
  });
  if (cat === 0) w.total = count;
  else w[CAT[cat]] = count;
}

// counts -> percentages of all 16+ residents
const list = Object.values(wards);
for (const w of list) {
  for (const f of Object.values(CAT)) w[f] = +((w[f] / w.total) * 100).toFixed(1);
}
// rank by % no qualifications, highest = #1 (most skills-deprived)
list.sort((a, b) => b.qual_none - a.qual_none).forEach((w, i) => { w.edu_rank = i + 1; });
// skills decile from % no qualifications: 1 = least deprived, 10 = most (highest no-quals)
list.slice().sort((a, b) => a.qual_none - b.qual_none)
  .forEach((w, i) => { w.skills_decile = Math.min(10, Math.ceil((i + 1) / (list.length / 10))); });
list.sort((a, b) => a.ward_code.localeCompare(b.ward_code));

if (list.length !== 69) throw new Error(`expected 69 wards, got ${list.length}`);

const rows = list.map(w =>
  `  { ward_code: '${w.ward_code}', ward_name: ${JSON.stringify(w.ward_name)}, ` +
  `qual_none: ${w.qual_none}, qual_level1: ${w.qual_level1}, qual_level2: ${w.qual_level2}, ` +
  `qual_apprenticeship: ${w.qual_apprenticeship}, qual_level3: ${w.qual_level3}, ` +
  `qual_level4plus: ${w.qual_level4plus}, qual_other: ${w.qual_other}, ` +
  `skills_decile: ${w.skills_decile}, edu_rank: ${w.edu_rank} },`
).join('\n');

const out = `// Birmingham ward education data — committed snapshot (source of truth, read directly).
//
// Qualifications: ONS Census 2021, table TS067 (Highest level of qualification),
//   via NOMIS dataset NM_2084_1. % of all usual residents aged 16+. 69 wards,
//   official 2022 ward codes (E05011118-E05011186). Pulled ${new Date().toISOString().slice(0, 10)}.
//   Census 2021 is frozen until the 2031 census — refresh only on an ONS restatement.
// skills_decile: derived from % no qualifications (1 = least skills-deprived,
//   10 = most). Not an external dataset — computed from the quals above.
//
// Regenerated by scripts/fetch-education-data.mjs.
import type { EducationWard } from './types';

export const EDUCATION_2021: EducationWard[] = [
${rows}
];
`;

writeFileSync(new URL('../lib/education-data.ts', import.meta.url), out);
console.log(`Wrote lib/education-data.ts — ${list.length} wards.`);
console.log('Sample:', list.find(w => w.ward_name === 'Ladywood'));
